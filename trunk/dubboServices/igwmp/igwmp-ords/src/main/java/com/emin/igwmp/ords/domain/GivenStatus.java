package com.emin.igwmp.ords.domain;

public enum GivenStatus {

	/**
	 * 正常
	 */
	NORMAL("正常"),
	/**
	 * 转赠中
	 */
	GIVING("转赠中"),
	/**
	 * 已赠送
	 */
	GIVEN("已赠送"),
	/**
	 * 待领取
	 */
	UNCLAIMED("待领取"),
	
	/**
	 * 赠送被退回
	 */
	UNTREAD("已退回");
	
	
	
	private String name;
	
	private GivenStatus(String name){
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
